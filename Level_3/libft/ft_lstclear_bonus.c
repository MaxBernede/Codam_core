#include <stdlib.h>
#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;
	t_list	*nfree;

	node = *lst;
	while (node)
	{
		nfree = node->next;
		ft_lstdelone(node, del);
		node = nfree;
	}
	*lst = NULL;
}
